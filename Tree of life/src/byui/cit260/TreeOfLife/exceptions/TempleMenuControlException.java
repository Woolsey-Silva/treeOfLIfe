package byui.cit260.TreeOfLife.exceptions;

/**
 *
 * @author ENTELLiJENSE
 */
public class TempleMenuControlException extends Exception {

    public TempleMenuControlException() {
    }

    public TempleMenuControlException(String message) {
        super(message);
    }

    public TempleMenuControlException(String message, Throwable cause) {
        super(message, cause);
    }

    public TempleMenuControlException(Throwable cause) {
        super(cause);
    }

    public TempleMenuControlException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

}
